//Basics Of String
#include <iostream>
using namespace std;
int main(){
    char str1[4]="C++";
    char str2[]={'C',' ','+','+','\0'};
    char str3[4]={'C','+','+','\0'};
    string str4 ="C++";
    cout<<str1<<endl;
    cout<<str2<<endl;
    cout<<str3<<endl;
    cout<<str4<<endl;
}
